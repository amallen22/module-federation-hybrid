export const htmlToString = (value: string) => {
    return value
    .replace(/(<p>)/gi, '')
    .replace(/(<\/p>)/gi, '\n')
    .replace(/(<ul([^>]+)>)/gi, '')
    .replace(/(<ul>)/gi, '')
    .replace(/(<\/ul>)/gi, '')
    .replace(/(<ol>)/gi, '')
    .replace(/(<\/ol>)/gi, '')
    .replace(/(<div([^>]+)>)/gi, '')
    .replace(/(<div>)/gi, '')
    .replace(/(<\/div>)/gi, '\n')
    .replace(/(<li([^>]+)>)/gi, '-')
    .replace(/(<li>)/gi, '-')
    .replace(/(<\/li>)/gi, '\n')
    .replace(/(<b>)/gi, '')
    .replace(/(<\/b>)/gi, '')
    .replace(/(<i>)/gi, '')
    .replace(/(<\/i>)/gi, '')
    .replace(/(<u>)/gi, '')
    .replace(/(<\/u>)/gi, '')
    .replace(/(<em>)/gi, '')
    .replace(/(<em([^>]+)>)/gi, '')
    .replace(/(<\/em>)/gi, '')
    .replace(/(<strong>)/gi, '')
    .replace(/(<strong([^>]+)>)/gi, '')
    .replace(/(<\/strong>)/gi, '')
    .replace(/(<font([^>]+)>)/gi, '')
    .replace(/(<\/font>)/gi, '')
    .replace(/(<i([^>]+)>)/gi, '')
    .replace(/(<\/i>)/gi, '')
    .replace(/(<b([^>]+)>)/gi, '')
    .replace(/(<\/b>)/gi, '')
    .replace(/(<br>)/gi, '\n')
    .replaceAll('&amp;', '&');
};
